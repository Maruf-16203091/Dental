<center><h1>Orthodontic Treatment</h1></center><br>
<left><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Orthodontic Treatment is done to repair and save badly infected or decayed tooth. It is needed when the soft tissue inside the tooth structure becomes inflamed or infected.

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Once the decay erodes the first two layers of tooth, that is, enamel and dentin, infection then reaches the nerve and pulp of tooth which then causes pain. If left untreated, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;infection may spread beyond ends of roots of tooth. Bone loss may also occur around the tooth which may lead to pus formation or an abscess.</p>
<br><h1>&nbsp;&nbsp;&nbsp;When Orthodontic Treatment May Needed?</h1><br>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.If there is severe toothache upon chewing.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.Swelling and tenderness in nearby gums of a particular tooth.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.Prolonged sensitivity to hot or cold.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4.Discoloration of a particular tooth.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5.Fractured or broken tooth.</left><br><br></p>
<h1>&nbsp;&nbsp;&nbsp;Procedure</h1><br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;An X-ray is always needed first to make a diagnosis if RCT is needed or not. The dentist then anesthetizes the tooth. A hole is then prepared in the tooth to access the root canals &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;for cleaning. After the infection has subsided the root canal is then filled with a biocompatible rubber compound- Gutta Percha.

While having root canal treatment, avoid chewing &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;or biting down on hard foods until treatment is completed.

However, tooth decay can be avoided by decreasing the frequency of sugar intake and by having regular check- ups.

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;RCT is a good treatment option to save the infected tooth. Otherwise the tooth may have to be extracted, and it is always better to keep the natural tooth.</p>
