<center><h1>Dental Implants</h1></center><br>
<left><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dental implants are metal posts or frames that are placed in the oral cavity. These are made up of titanium which is a non reactive material and is well accepted by body cells.
<br><h1>&nbsp;&nbsp;&nbsp;When Dental Implants May Needed?</h1><br>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Teeth play a vital role in the overall general health and social life of a person. So losing one’s teeth affects the quality of life to a great extent. The most basic being affected is nutrition and taste. Besides this, after loss of teeth the remaining bone also undergoes rapid degeneration.

A person’s social life is also affected to a great extent due to absence of teeth. While a beautiful smile fills you with confidence, lack of it diminishes it.
</left><br><br></p>
<h1>&nbsp;&nbsp;&nbsp;Treatment Options</h1><br>
<p>
There are various methods to replace the lost teeth.

In case of loss of single tooth, bridges can be used.
When there is loss of more than one tooth then removable partial dentures can also be given.
In case of absence of all teeth, complete dentures with or without implants can be given.
Advantages of Implants
Implants are the most natural looking and stable replacements for lost teeth.
No clasps or visible attachments are required.
No cutting down of adjacent natural teeth is required.Since implants share the functional load during chewing, they contribute to maintaining the health of other teeth.
Implants are ideal replacements for preserving the health of the remaining bone left after loss of teeth.
They do not shift or slip like removable partial dentures or complete dentures.
Nearly the same shade as natural tooth can be acquired.
Provides a long term solution.
With all these benefits, implants are nearly ideal solutions for problems related to loss of teeth, thereby enhancing your confidence and giving you that smile back.

Types of Dental Implants
Root Form: These screw shaped implants resemble tooth roots in form. These are used when sufficient depth of bone is present.
Plate Form: These are wider in form and are used with narrow bone.
Subperiosteal: These are placed just beneath the gums and are used in case of advanced resorption of bone.
Procedure
Placement of implants is a procedure with minimal discomfort. The oral surgeon/implantologist gives anaesthesia, then exposes the bone by giving incision. The implant is placed inside the bone, the incision is then stitched back.

After this, it requires a healing period of about 3- 6 months. During this period, a process known as ‘osseointegration’ occurs in which bone cells adapt themselves around implant material.

After the healing period, the crown is then placed over the implant.

Bone Grafts for Implants
Implants can be placed even if the remaining bone is reduced in length and width. It may then require a procedure known as bone grafting.
